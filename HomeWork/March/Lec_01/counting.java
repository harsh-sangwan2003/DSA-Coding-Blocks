package HomeWork.March.Lec_01;
import java.util.Scanner;

public class counting {

    public static void main(String[] args) {

        Scanner scn = new Scanner(System.in);
        int n = scn.nextInt();

        int i = 1;

        while(i<=n){
            System.out.println(i);
            i++;
        }
    }
}
